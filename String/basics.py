string = "   Hello there python   "
# pring string
print(string)
# count length
print(len(string))
# slicing
print(string[5:10])
# split string
print(string.split())
# startswith
print(string.startswith("H"))
# endswith
print(string.endswith(""))
# repeat string
print("."*10)
# replacing
print(string.replace("l","00"))
# strip string
print(string.strip())
# left strip
print(string.lstrip())
# right strip
print(string.rstrip())
# change uppercase
print(string.upper())
# change lowercase
print(string.lower())
# change title case
print(string.title())
# change capitalcase
print(string.capitalize())
# change swapcase
print(string.swapcase())
# reversed method
print(''.join(reversed(string)))
# string join
print('-'.join(string))
# string concatenation
print(string + "OKAY THER BRO")